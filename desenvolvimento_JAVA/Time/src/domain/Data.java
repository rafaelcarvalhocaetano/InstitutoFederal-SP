
package domain;

import java.util.Date;

/**
 *
 * @author Rafael Carvalho
 */
public class Data {
    
    
    private int id;
    private Date data;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the data
     */
    public Date getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(Date data) {
        this.data = data;
    }
    
    
    
}
