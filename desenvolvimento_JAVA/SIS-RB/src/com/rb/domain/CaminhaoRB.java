package com.rb.domain;

/**
 *
 * @author Rafael Carvalho Caetano
 */
public class CaminhaoRB {
    
    private int id;
    private String cavalo;
    private String carreta;
    private String nome;
    private String data;
    private String saida;
    private String comosaida;
    private String entrada;
    private String comoentrada;
    private String destino;
    private String lacre;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the cavalo
     */
    public String getCavalo() {
        return cavalo;
    }

    /**
     * @param cavalo the cavalo to set
     */
    public void setCavalo(String cavalo) {
        this.cavalo = cavalo;
    }

    /**
     * @return the carreta
     */
    public String getCarreta() {
        return carreta;
    }

    /**
     * @param carreta the carreta to set
     */
    public void setCarreta(String carreta) {
        this.carreta = carreta;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

    /**
     * @return the saida
     */
    public String getSaida() {
        return saida;
    }

    /**
     * @param saida the saida to set
     */
    public void setSaida(String saida) {
        this.saida = saida;
    }

    /**
     * @return the comosaida
     */
    public String getComosaida() {
        return comosaida;
    }

    /**
     * @param comosaida the comosaida to set
     */
    public void setComosaida(String comosaida) {
        this.comosaida = comosaida;
    }

    /**
     * @return the entrada
     */
    public String getEntrada() {
        return entrada;
    }

    /**
     * @param entrada the entrada to set
     */
    public void setEntrada(String entrada) {
        this.entrada = entrada;
    }

    /**
     * @return the comoentrada
     */
    public String getComoentrada() {
        return comoentrada;
    }

    /**
     * @param comoentrada the comoentrada to set
     */
    public void setComoentrada(String comoentrada) {
        this.comoentrada = comoentrada;
    }

    /**
     * @return the destino
     */
    public String getDestino() {
        return destino;
    }

    /**
     * @param destino the destino to set
     */
    public void setDestino(String destino) {
        this.destino = destino;
    }

    /**
     * @return the lacre
     */
    public String getLacre() {
        return lacre;
    }

    /**
     * @param lacre the lacre to set
     */
    public void setLacre(String lacre) {
        this.lacre = lacre;
    }
}
