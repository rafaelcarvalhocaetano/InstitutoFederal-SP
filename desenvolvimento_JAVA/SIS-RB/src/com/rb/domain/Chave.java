package com.rb.domain;

/**
 *
 * @author Rafael Carvalho Caetano
 */
public class Chave {
    
    
    private Integer id;
    private String nome;
    private String setor;
    private String dataretirada;
    private String hsaida;
    private String hentrada;
    private String dataDevolucao;
    private String vigilante;

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the setor
     */
    public String getSetor() {
        return setor;
    }

    /**
     * @param setor the setor to set
     */
    public void setSetor(String setor) {
        this.setor = setor;
    }

    /**
     * @return the dataretirada
     */
    public String getDataretirada() {
        return dataretirada;
    }

    /**
     * @param dataretirada the dataretirada to set
     */
    public void setDataretirada(String dataretirada) {
        this.dataretirada = dataretirada;
    }

    /**
     * @return the hsaida
     */
    public String getHsaida() {
        return hsaida;
    }

    /**
     * @param hsaida the hsaida to set
     */
    public void setHsaida(String hsaida) {
        this.hsaida = hsaida;
    }

    /**
     * @return the hentrada
     */
    public String getHentrada() {
        return hentrada;
    }

    /**
     * @param hentrada the hentrada to set
     */
    public void setHentrada(String hentrada) {
        this.hentrada = hentrada;
    }

    /**
     * @return the dataDevolucao
     */
    public String getDataDevolucao() {
        return dataDevolucao;
    }

    /**
     * @param dataDevolucao the dataDevolucao to set
     */
    public void setDataDevolucao(String dataDevolucao) {
        this.dataDevolucao = dataDevolucao;
    }

    /**
     * @return the vigilante
     */
    public String getVigilante() {
        return vigilante;
    }

    /**
     * @param vigilante the vigilante to set
     */
    public void setVigilante(String vigilante) {
        this.vigilante = vigilante;
    }
    
    
    
}
